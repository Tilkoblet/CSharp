                _rest.AddBody("CONFIRM_DT", "");
				_rest.AddBody("FM_CD", "");
				_rest.AddBody("RGST_CHRGP_ID", "");

				// API 호출
				string _result = _rest.Call();
				if (_rest.HttpStatusCode != System.Net.HttpStatusCode.OK)
				{
					throw new Exception(_rest.Message);
				}
			}
			catch (Exception err)
			{
				Debug.WriteLine(err.Message);
			}
		}
	}
