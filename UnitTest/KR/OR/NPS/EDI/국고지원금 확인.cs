	public class 국고지원금확인
	{
		[TestMethod]
		public void TILKO_API()
		{
			try
			{
				Tilko.API.REST _rest = new Tilko.API.REST(Constant.ApiKey);
				_rest.Init();

				// 국민연금 EDI의 국고지원금 확인 endPoint 설정
				_rest.SetEndPointUrl(Constant.ApiHost + "api/v1.0/npsedi/u040209m01");

